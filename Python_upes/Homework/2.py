print("Hello\tWorld")   # Tab space
print("C:\\Users\\Admin")  # Backslash
print("It\'s a nice day")  # Single quote
print("He said, \"Hello!\"")  # Double quote
print("Hello\rWorld")  # Carriage return (World replaces Hello)
print("Hello\bWorld")  # Backspace (Deletes 'o')
print("Hello\fWorld")  # Form feed (page break)
print("Hello\vWorld")  # Vertical tab
print("\101")  # Octal value for 'A'
print("\x41")  # Hexadecimal value for 'A'